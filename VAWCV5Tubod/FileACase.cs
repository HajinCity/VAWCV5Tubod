        private static string BuildFullName(string lastName, string firstName, string middleName)
        {
            return string.Join(
                " ",
                new[] { lastName.Trim(), firstName.Trim(), middleName.Trim() }
                    .Where(part => !string.IsNullOrWhiteSpace(part)));
        }

