        private static int CalculateAge(DateTime birthDate)
        {
            DateTime today = AutoGeneratedCaseId.GetPhilippineToday();
            int age = today.Year - birthDate.Year;

            if (birthDate.Date > today.AddYears(-age))
            {
                age--;
            }

            return Math.Max(age, 0);
        }

