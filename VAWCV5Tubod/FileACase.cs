        private void saveACasebtn_Click(object? sender, EventArgs e)
        {
            if (!ValidateRequiredInputs())
            {
                return;
            }

            try
            {
                using MySqlConnection connection = DbConnectionFactory.CreateConnection();
                connection.Open();

                using MySqlTransaction transaction = connection.BeginTransaction();

                int complainantId = SaveComplainant(connection, transaction);
                int respondentId = SaveRespondent(connection, transaction);
                int caseId = SaveCaseData(connection, transaction, complainantId, respondentId);

                transaction.Commit();

                UserLogService.Log(
                    currentUsername,
                    "AddCase",
                    "caselist",
                    caseId,
                    $"Created new case record for {BuildFullName(comp_lastname.Text, comp_firstname.Text, comp_middlename.Text)}.");

                MessageBox.Show(
                    "Case information was saved successfully.",
                    "Save Successful",
                    MessageBoxButtons.OK,
                    MessageBoxIcon.Information);

                ResetFormInputs();
                RefreshGeneratedCaseDetails();
            }
            catch (Exception ex)
            {
                MessageBox.Show(
                    $"Unable to save the case.{Environment.NewLine}{ex.Message}",
                    "Save Failed",
                    MessageBoxButtons.OK,
                    MessageBoxIcon.Error);
            }
        }

