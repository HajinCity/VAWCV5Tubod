            ValidateRequiredText(resp_civilstatus, "Respondent Civil Status", missingFields);
            ValidateRequiredText(resp_religion, "Respondent Religion", missingFields);
            ValidateRequiredText(resp_occupation, "Respondent Occupation", missingFields);
            ValidateRequiredText(resp_nationality, "Respondent Nationality", missingFields);
            ValidateRequiredText(resp_educationalattainment, "Respondent Educational Attainment", missingFields);
            ValidateRequiredText(resp_relationship_to_complainant, "Relationship To Complainant", missingFields);

            ValidateRequiredText(case_ra_violation, "R.A. Violation", missingFields);
            ValidateRequiredText(case_status, "Case Status", missingFields);
            ValidateRequiredText(case_referred_to, "Referred To", missingFields);
            ValidateRequiredText(case_incident_place, "Place Of Incident", missingFields);
            ValidateRequiredText(case_incident_description, "Incident Description", missingFields);

            if (comp_image.Image is null)
            {
                missingFields.Add("Complainant Image");
            }

            if (resp_image.Image is null)
            {
                missingFields.Add("Respondent Image");
            }

            if (IsRa9262Selected() &&
                !case_ra_sub_physical_abuse.Checked &&
                !case_ra_sub_psychological_abuse.Checked &&
                !case_ra_sub_sexual_abuse.Checked &&
                !case_ra_sub_economic_abuse.Checked)
            {
                missingFields.Add("At least one R.A. Sub Case Violation");
            }

            if (string.IsNullOrWhiteSpace(label4.Text) || label4.Text == "Unavailable")
            {
                missingFields.Add("Case ID");
            }

            if (missingFields.Count == 0)
            {
                return true;
            }

            MessageBox.Show(
                "Please complete all required fields before saving:" +
                Environment.NewLine + Environment.NewLine +
                string.Join(Environment.NewLine, missingFields.Select(field => $"- {field}")),
                "Incomplete Form",
                MessageBoxButtons.OK,
                MessageBoxIcon.Warning);

            return false;
        }

        private static void ValidateRequiredText(Control control, string fieldName, List<string> missingFields)
        {
            if (string.IsNullOrWhiteSpace(control.Text))
            {
                missingFields.Add(fieldName);
            }
        }

        private void ResetFormInputs()
        {
            ClearTextBoxes(this);
            ClearComboBoxes(this);
            ResetDateTimePickers(this);
            ClearCheckBoxes(this);
            ClearImages();
            UpdateAgeFields();
            ToggleRa9262SubViolations();
        }

        private void RefreshGeneratedCaseDetails()
        {
            label6.Text = AutoGeneratedCaseId.GetPhilippineDateToday();

            try
            {
                label4.Text = AutoGeneratedCaseId.GenerateCaseId();
            }
            catch
            {
                label4.Text = "Unavailable";
            }
        }

        private void ClearTextBoxes(Control parent)
        {
            foreach (Control control in parent.Controls)
            {
                if (control is TextBox textBox)
                {
                    textBox.Clear();
                }

                if (control.HasChildren)
                {
                    ClearTextBoxes(control);
                }
            }
        }

        private void ClearComboBoxes(Control parent)
        {
            foreach (Control control in parent.Controls)
            {
                if (control is ComboBox comboBox)
                {
                    comboBox.SelectedIndex = -1;
                    comboBox.Text = string.Empty;
                }

                if (control.HasChildren)
                {
                    ClearComboBoxes(control);
                }
            }
        }

        private void ResetDateTimePickers(Control parent)
        {
            foreach (Control control in parent.Controls)
            {
                if (control is DateTimePicker dateTimePicker)
                {
                    dateTimePicker.Value = DateTime.Now;
                }

                if (control.HasChildren)
                {
                    ResetDateTimePickers(control);
                }
            }
        }

        private void ClearCheckBoxes(Control parent)
        {
            foreach (Control control in parent.Controls)
            {
                if (control is CheckBox checkBox)
                {
                    checkBox.Checked = false;
                }

                if (control.HasChildren)
                {
                    ClearCheckBoxes(control);
                }
            }
        }

        private void ClearImages()
        {
            Image? complainantImage = comp_image.Image;
            Image? respondentImage = resp_image.Image;

            comp_image.Image = null;
            resp_image.Image = null;

            complainantImage?.Dispose();
            respondentImage?.Dispose();
        }

        private static byte[]? ConvertImageToBytes(Image? image)
        {
            if (image is null)
            {
                return null;
            }

            using MemoryStream memoryStream = new();
            image.Save(memoryStream, ImageFormat.Png);
            return memoryStream.ToArray();
        }

        private void saveACasebtn_Click_1(object sender, EventArgs e)
        {
            if (!ValidateRequiredInputs())
            {
                return;
            }

            try
            {
                using MySqlConnection connection = DbConnectionFactory.CreateConnection();
                connection.Open();

                using MySqlTransaction transaction = connection.BeginTransaction();

                int complainantId = SaveComplainant(connection, transaction);
                int respondentId = SaveRespondent(connection, transaction);
                SaveCaseData(connection, transaction, complainantId, respondentId);

                transaction.Commit();

                MessageBox.Show(
                    "Case information was saved successfully.",
                    "Save Successful",
                    MessageBoxButtons.OK,
                    MessageBoxIcon.Information);

                ResetFormInputs();
                RefreshGeneratedCaseDetails();
            }
            catch (Exception ex)
            {
                MessageBox.Show(
                    $"Unable to save the case.{Environment.NewLine}{ex.Message}",
                    "Save Failed",
                    MessageBoxButtons.OK,
                    MessageBoxIcon.Error);
            }
        }
